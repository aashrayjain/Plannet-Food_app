    private void loadCategories(){
        try{
            categories=CategoryDao.getAllCategoryId();
            for(String catId:categories.values())
                jcCatId.addItem(catId);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Some Problem in DB!!","Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
    
    private boolean validateInput(){
        String catName=txtCatName.getText();
        if(catName.isEmpty())
            return false;
        return true;
    }
