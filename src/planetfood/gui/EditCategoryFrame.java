    private void btnSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangesActionPerformed
        if(!this.validateInput()){
            JOptionPane.showMessageDialog(null, "Please enter the category name","Empty Field",JOptionPane.ERROR_MESSAGE); 
            return;
        }
        try{
            String catId=jcCatId.getSelectedItem().toString();
            String catName=txtCatName.getText();
            boolean result=CategoryDao.updateCategory(catName, catId);
            if(result==true){
                JOptionPane.showMessageDialog(null, "Category name saved successfully!!","Success!",JOptionPane.INFORMATION_MESSAGE); 
                this.loadCategories();
            }
            else
                JOptionPane.showMessageDialog(null, "Cannot save category name!!","Error!",JOptionPane.ERROR_MESSAGE); 
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Some problem in DB!!","Error!",JOptionPane.ERROR_MESSAGE); 
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveChangesActionPerformed

