    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if(txtEmpId.equals("")){
            JOptionPane.showMessageDialog(null,"Employee Id cannot be Empty","Empty fields!",JOptionPane.ERROR_MESSAGE );
            return;
        }
        try{
            empId=txtEmpId.getText();
            Emp e=EmpDao.findEmployeeById(empId);
            if(e==null){
                JOptionPane.showMessageDialog(null,"No employee present","Error",JOptionPane.ERROR_MESSAGE );
                return;
            }
            DefaultTableModel model=(DefaultTableModel)jtEmployeeDetail.getModel();
            Object[] rows=new Object[4];
            rows[0]=e.getEmpid();
            rows[1]=e.getEmpname();
            rows[2]=e.getJob();
            rows[3]=e.getSal();
            model.addRow(rows);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error","Error!",JOptionPane.ERROR_MESSAGE );
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

