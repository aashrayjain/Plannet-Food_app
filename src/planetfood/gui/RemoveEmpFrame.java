    private void btnDeleteEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteEmpActionPerformed
        DefaultTableModel model=(DefaultTableModel)jtEmployeeDetail.getModel();
        Object [] rows=new Object[4];
        for(int i=0;i<model.getColumnCount();i++){
            rows[i]=model.getValueAt(0, i);
        }
        try{
            if(EmpDao.removeEmployee((String)rows[0])){
                JOptionPane.showMessageDialog(null,"Employee successfully removed","Success",JOptionPane.INFORMATION_MESSAGE );
            }
            model.removeRow(0);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Some Error occur","Error!",JOptionPane.ERROR_MESSAGE );
            ex.printStackTrace();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Some Error occur","Error!",JOptionPane.ERROR_MESSAGE );
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteEmpActionPerformed

