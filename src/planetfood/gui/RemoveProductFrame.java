    private void jcProdCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcProdCatActionPerformed
        catName=jcProdCat.getSelectedItem().toString();
        catId=categories.get(catName);
        try{
            productList=ProductDao.getActiveProductsByCategory(catId);
            jcProdId.removeAllItems();
            for(String prodName:productList.keySet())
                jcProdId.addItem(prodName);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Some problem in DB", "Error!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcProdCatActionPerformed

