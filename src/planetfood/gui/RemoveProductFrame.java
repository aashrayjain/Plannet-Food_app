    private void btnRemoveProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveProductActionPerformed
        catName=jcProdCat.getSelectedItem().toString();
        catId=categories.get(catName);
        try{
            String prodName=jcProdId.getSelectedItem().toString();
            String prodId=productList.get(prodName);
            if(ProductDao.removeProduct(prodId)==true){
                JOptionPane.showMessageDialog(null, "Product successfully removed", "Message", JOptionPane.INFORMATION_MESSAGE);
                this.loadCategories();
            }
            else{
                JOptionPane.showMessageDialog(null, "Product could not be removed", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Some problem in DB!", "Error!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRemoveProductActionPerformed

