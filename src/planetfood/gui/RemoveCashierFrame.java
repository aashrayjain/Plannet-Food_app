    private void btnDeleteCashierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCashierActionPerformed
        DefaultTableModel model=(DefaultTableModel)jtRemoveCashier.getModel();
        Object [] rows=new Object[3];
        for(int i=0;i<model.getColumnCount();i++){
            rows[i]=model.getValueAt(0, i);
        }
        try{
            if(UserDao.removeCashier((String)rows[2])){
                JOptionPane.showMessageDialog(null,"Cashier successfully removed","Success",JOptionPane.INFORMATION_MESSAGE );
            }
            model.removeRow(0);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error occur","Error!",JOptionPane.ERROR_MESSAGE );
            ex.printStackTrace();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Some Error occur","Error!",JOptionPane.ERROR_MESSAGE );
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteCashierActionPerformed

