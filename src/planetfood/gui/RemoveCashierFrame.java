    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        userId=txtUserId.getText();
        if(userId.isEmpty()){
            JOptionPane.showMessageDialog(null,"Employee Id cannot be Empty","Empty fields!",JOptionPane.ERROR_MESSAGE );
            return;
        }
        try{
            empId=UserDao.getIdByUserId(userId);
            if(empId==null){
                JOptionPane.showMessageDialog(null,"No cashier present","Error",JOptionPane.ERROR_MESSAGE );
                txtUserId.setText("");
                return;
            }
            DefaultTableModel model=(DefaultTableModel)jtRemoveCashier.getModel();
            Object[] rows=new Object[3];
            rows[0]=userId;
            rows[1]=EmpDao.findEmployeeById(empId).getEmpname();
            rows[2]=empId;
            model.addRow(rows);
            txtUserId.setText("");
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error","Error!",JOptionPane.ERROR_MESSAGE );
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

